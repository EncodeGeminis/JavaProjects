public class Operaciones {
    //metodos
    public static int sumar(int a, int b){
        System.out.println("Metodo sumar argumentos enteros");
        return a+b;
    }
    //metodo sobrecargado
    public static double sumar(double a, double b){
        System.out.println("Metodo con argumentos double");
        return a+b;
    }
}
